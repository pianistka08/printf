#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int			i;
	char		*temp;

	temp = s1;
	i = 0;
	s1 = s1 + ft_strlen(s1);
	while (s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (temp);
}
