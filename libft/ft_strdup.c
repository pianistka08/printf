#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*copy;
	int		i;

	copy = (char *)malloc(ft_strlen((char *)str) + 1);
	if (copy == NULL)
		return (0);
	i = 0;
	while (str[i])
	{
		copy[i] = str[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
